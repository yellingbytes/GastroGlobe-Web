export function cuisinesForCountry(countryId) {
  const grouped = new Map();
  restaurants
    .filter((restaurant) => restaurant.countryId === countryId)
    .forEach((restaurant) => {
      if (!grouped.has(restaurant.cuisineId)) {
        grouped.set(restaurant.cuisineId, {
          id: restaurant.cuisineId,
          name: restaurant.cuisine,
          symbol: restaurant.symbol,
          restaurants: [],
        });
      }
      grouped.get(restaurant.cuisineId).restaurants.push(restaurant);
    });
  return [...grouped.values()];
}

export function googleMapsUrl(restaurant) {
  const query = encodeURIComponent(`${restaurant.name}, ${restaurant.address}`);
  return `https://www.google.com/maps/search/?api=1&query=${query}`;
}
